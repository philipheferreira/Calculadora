

namespace principal { 

    class calculadora{  
        public int soma(int x, int y) { 
        return x + y;
        }
        public int sub(int x, int y) {
            return x - y;
        }

        public int mult(int x, int y) { 
            return x * y;
        }

        public float div(float x, float y) {
            return x / y;
        }
    }

}